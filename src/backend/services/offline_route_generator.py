from __future__ import annotations

import json

_TEMPLATE = '''\
#!/usr/bin/env python3
"""
Auto-generated offline route executor for Kachaka Playground container.
DO NOT EDIT — generated by OfflineRouteGenerator.
"""
import json
import threading
import time
import urllib.request

import kachaka_api

# ── Embedded parameters ────────────────────────────────────────────────────────
RUN_ID = {run_id!r}
STOPS = json.loads({stops_json!r})
SHELF_NAME = {shelf_name!r}
DEFAULT_TIMEOUT = {default_timeout!r}
PI_URL = {pi_url!r}

# ── gRPC client (container-internal address) ───────────────────────────────────
GRPC_ADDRESS = "100.94.1.1:26400"
client = kachaka_api.KachakaApiClient(GRPC_ADDRESS)

# ── IMU shake detection (yaw-rate zero-crossing in sliding window) ────────────
# Distinguishes "shake" (operator wrist oscillation → many sign-reversals in gz)
# from "push" (single-direction displacement → monotonic gz offset, no crossings).
# Validated 2026-05-21: push gz_zc/window=1, shake1 gz_zc/window=2, shake gz_zc/window=5.
GZ_MIN_PEAK = 0.2        # rad/s — crossing needs at least one side above this (rejects noise flips around 0)
WINDOW_SAMPLES = 15      # ~1.5 s at the IMU's ~10 Hz publish rate
ZC_THRESHOLD = 2         # filtered zero-crossings in window required to trigger

_imu_armed = False
_imu_lock = threading.Lock()
_shake_event = threading.Event()
_gz_buffer = []
_imu_thread_stop = threading.Event()


def _imu_worker():
    """Background thread: cursor-poll IMU and trigger shake_event when armed + oscillation detected."""
    cursor = 0
    while not _imu_thread_stop.is_set():
        try:
            req = kachaka_api.pb2.GetRequest(
                metadata=kachaka_api.pb2.Metadata(cursor=cursor))
            resp = client.stub.GetRosImu(req, timeout=2.0)
            cursor = resp.metadata.cursor
            gz = resp.imu.angular_velocity.z

            with _imu_lock:
                _gz_buffer.append(gz)
                if len(_gz_buffer) > WINDOW_SAMPLES:
                    _gz_buffer.pop(0)
                armed = _imu_armed
                if armed and len(_gz_buffer) == WINDOW_SAMPLES:
                    zc = 0
                    for i in range(1, WINDOW_SAMPLES):
                        a, b = _gz_buffer[i - 1], _gz_buffer[i]
                        if (a >= 0) != (b >= 0) and (abs(a) > GZ_MIN_PEAK or abs(b) > GZ_MIN_PEAK):
                            zc += 1
                    if zc >= ZC_THRESHOLD:
                        _shake_event.set()
        except Exception:
            pass


def _arm_imu(settle_delay=2.0):
    global _imu_armed
    _shake_event.clear()
    with _imu_lock:
        _gz_buffer.clear()
    time.sleep(settle_delay)
    with _imu_lock:
        _imu_armed = True


def _disarm_imu():
    global _imu_armed
    with _imu_lock:
        _imu_armed = False
    _shake_event.clear()


# ── Report client ──────────────────────────────────────────────────────────────
_REPORT_URL = PI_URL + "/api/routes/offline/report"


def try_report(event, stop_index=None, retries=0):
    """HTTP POST event to Pi. Silent on failure. Retries for terminal events."""
    payload = {{"run_id": RUN_ID, "event": event, "stop_index": stop_index,
               "timestamp": time.strftime("%Y-%m-%dT%H:%M:%S")}}
    data = json.dumps(payload).encode()
    attempts = 1 + retries
    for attempt in range(attempts):
        try:
            req = urllib.request.Request(
                _REPORT_URL, data=data,
                headers={{"Content-Type": "application/json"}}, method="POST",
            )
            with urllib.request.urlopen(req, timeout=5):
                return True
        except Exception:
            if attempt < attempts - 1:
                time.sleep(10)
    return False


# ── Main ───────────────────────────────────────────────────────────────────────
# NOTE: kachaka-api SDK move_shelf/return_shelf/return_home are BLOCKING calls
# (they wait until the command completes before returning). No extra polling needed.

def main():
    print(f"[route_executor] Starting route {{RUN_ID}}", flush=True)
    print(f"[route_executor] Shelf: {{SHELF_NAME}}, Stops: {{len(STOPS)}}", flush=True)

    # Initialize name->ID resolver (required before name-based commands)
    print("[route_executor] Initializing resolver...", flush=True)
    client.update_resolver()
    print("[route_executor] Resolver ready", flush=True)

    imu_thread = threading.Thread(target=_imu_worker, daemon=True)
    imu_thread.start()

    try:
        for i, stop in enumerate(STOPS):
            stop_name = stop["name"]
            timeout_sec = stop.get("timeout_sec") or DEFAULT_TIMEOUT
            print(f"[route_executor] Stop {{i+1}}/{{len(STOPS)}}: {{stop_name}}", flush=True)

            # Move shelf to stop (blocking — waits until arrival)
            try_report("moving", i)
            print(f"[route_executor] Moving shelf to {{stop_name}}...", flush=True)
            result = client.move_shelf(SHELF_NAME, stop_name)
            print(f"[route_executor] move_shelf returned: {{result}}", flush=True)
            try_report("arrived", i)

            # Announce arrival
            client.speak("到站，請取貨")
            print(f"[route_executor] Waiting for shake or timeout ({{timeout_sec}}s)", flush=True)

            # Arm IMU with 2s settle delay, then wait
            _arm_imu(settle_delay=2.0)
            shook = _shake_event.wait(timeout=timeout_sec)
            _disarm_imu()

            if shook:
                print(f"[route_executor] Shake confirmed at {{stop_name}}", flush=True)
                try_report("shake_confirmed", i)
            else:
                print(f"[route_executor] Timeout at {{stop_name}}", flush=True)
                client.speak("超時，即將前往下一站")
                try_report("timeout", i)
                time.sleep(2.0)

        # Return shelf and go home (both blocking)
        print("[route_executor] Returning shelf...", flush=True)
        client.return_shelf(SHELF_NAME)
        print("[route_executor] Going home...", flush=True)
        client.return_home()
        print(f"[route_executor] Route {{RUN_ID}} completed, reporting...", flush=True)
        try_report("completed", retries=60)  # retry up to 60 times (10s apart = ~10 min)
        print(f"[route_executor] Done", flush=True)

    except Exception as exc:
        print(f"[route_executor] ERROR: {{exc}}", flush=True)
        try_report("failed", retries=60)
        try:
            client.return_shelf(SHELF_NAME)
        except Exception:
            pass
        try:
            client.return_home()
        except Exception:
            pass
    finally:
        _imu_thread_stop.set()
        imu_thread.join(timeout=2.0)


if __name__ == "__main__":
    main()
'''


class OfflineRouteGenerator:
    """Generates a self-contained Python script for the Kachaka Playground container."""

    def generate(
        self,
        run_id: str,
        stops: list[dict],
        shelf_name: str,
        default_timeout: int,
        pi_url: str,
    ) -> str:
        """Return a complete Python script string (route_executor.py content)."""
        stops_json = json.dumps(stops)
        return _TEMPLATE.format(
            run_id=run_id,
            stops_json=stops_json,
            shelf_name=shelf_name,
            default_timeout=default_timeout,
            pi_url=pi_url,
        )
